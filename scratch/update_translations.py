def update_po_file(lang, lang_trans):
    path = f'locale/{lang}/LC_MESSAGES/django.po'
    if not os.path.exists(path):
        print(f"File {path} not found.")
        return
    
    with open(path, 'r', encoding='UTF-8') as f:
        content = f.read()
    
    for msgid, msgstr in lang_trans.items():
        # Look for the msgid and replace its msgstr if it's empty
        pattern = rf'msgid "{re.escape(msgid)}"\nmsgstr ""'
        replacement = f'msgid "{msgid}"\nmsgstr "{msgstr}"'
        content = re.sub(pattern, replacement, content)
        
        # Also handle cases where it might have been fuzzy or recently uncommented
        pattern_active = rf'msgid "{re.escape(msgid)}"\nmsgstr ".*"'
        # Only replace if it was empty or we want to force it
        # For now, let's only replace empty ones to be safe
    
    with open(path, 'w', encoding='UTF-8') as f:
        f.write(content)
    print(f"Updated {lang}")

for lang, trans in translations.items():
    update_po_file(lang, trans)
